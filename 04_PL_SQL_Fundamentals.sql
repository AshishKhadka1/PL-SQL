--LABELS
DECLARE
    I NUMBER:= 0;
    J NUMBER:= 0;
BEGIN
    <<OUTER_LOOP>> 
    LOOP
        I := I+1;
        EXIT OUTER_LOOP WHEN I > 2;
        
        DBMS_OUTPUT.PUT_LINE('OUTER LOOP :'|| I);
        
        J:=0;
        <<INNER_LOOP>>
        LOOP
            J:= J + 1;
            EXIT INNER_LOOP WHEN J > 3;
            
            DBMS_OUTPUT.PUT_LINE('INNER LOOP :'||J);
            
        END LOOP INNER_LOOP;
        
        END LOOP OUTER_LOOP;
END;
/

